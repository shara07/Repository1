package XMLReader;
public class TestcaseHeader {
	private String Productfamily;
	private String Module;
	private String Scenario;
	private String Instance_name;
	private String Responsbility;
	private String Created_By;
	private String Created_On;
	private String Flow_Version;
	private String TestCaseName;
	private String TestCaseDescription;

	public String getProductfamily() {
		return Productfamily;
	}
	public void setProductfamily(String Productfamily) {
		this.Productfamily = Productfamily;
	}
	
	public String getModule() {
		return Module;
	}
	public void setModule(String module) {
		Module = module;
	}
	public String getScenario() {
		return Scenario;
	}
	public void setScenario(String scenario) {
		Scenario = scenario;
	}
	public String getInstance_name() {
		return Instance_name;
	}
	public void setInstance_name(String instance_name) {
		Instance_name = instance_name;
	}
	public String getResponsbility() {
		return Responsbility;
	}
	public void setResponsbility(String responsbility) {
		Responsbility = responsbility;
	}
	public String getCreated_By() {
		return Created_By;
	}
	public void setCreated_By(String created_By) {
		Created_By = created_By;
	}
	public String getCreated_On() {
		return Created_On;
	}
	public void setCreated_On(String created_On) {
		Created_On = created_On;
	}
	public String getFlow_Version() {
		return Flow_Version;
	}
	public void setFlow_Version(String flow_Version) {
		Flow_Version = flow_Version;
	}
	public String getTestCaseName() {
		return TestCaseName;
	}
	public void setTestCaseName(String testCaseName) {
		TestCaseName = testCaseName;
	}
	public String getTestCaseDescription() {
		return TestCaseDescription;
	}
	public void setTestCaseDescription(String testCaseDescription) {
		TestCaseDescription = testCaseDescription;
	}
	
}
